import { generateKeyPairSync } from 'crypto';

export default function handler(req, res) {
  const { publicKey, privateKey } = generateKeyPairSync('rsa', {
    modulusLength: 4096,
    publicKeyEncoding: {
      type: 'spki',
      format: 'pem'
    },
    privateKeyEncoding: {
      type: 'pkcs8',
      format: 'pem'
    }
  });

  res.status(200).json({ publicKey, privateKey });
}
